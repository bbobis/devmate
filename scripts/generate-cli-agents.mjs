// @ts-check
/**
 * Generate Copilot CLI agent files (.github/agents/*.md) from the Copilot Chat
 * agent definitions (agents/*.agent.md) that are the single source of truth.
 *
 * Why a generator (not copy/symlink):
 * - The Chat (.agent.md) and CLI (.github/agents/*.md) frontmatter shapes differ;
 *   a generator normalizes frontmatter while sharing the agent body verbatim.
 * - Symlinks break on Windows checkouts and Git does not preserve them reliably
 *   across platforms; copy-paste drifts. One source + one generator does not.
 *
 * Output is committed. `scripts/check-generated-cli-agents.mjs` regenerates in
 * memory and fails CI (git diff --exit-code) when the committed files drift.
 *
 * @see scripts/generate-docs.mjs for the sentinel/generation convention this mirrors.
 */
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { assertNodeVersion, isMainModule } from '../lib/env-guard.mjs';
import { ensureDirSync, readTextFileSync, writeTextFileSync } from '../lib/fs-safe.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');

/**
 * Chat agent -> CLI agent mapping. The body is shared verbatim; only frontmatter
 * is normalized. Add new shared agents here.
 * @type {Array<{ source: string, target: string }>}
 */
export const DEFAULT_AGENTS = [
  { source: 'agents/planner.agent.md', target: '.github/agents/planner.md' },
  { source: 'agents/rubber-duck.agent.md', target: '.github/agents/rubber-duck.md' },
];

/** @typedef {{ name?: string, description?: string, model?: string, raw: string }} Frontmatter */

/**
 * Parse a YAML-ish frontmatter block (lines between the first two `---` fences).
 * Only the flat string fields devmate uses are extracted; everything else is
 * dropped on emit. No nested structures are supported by design.
 * @param {string} text
 * @returns {{ frontmatter: Frontmatter, body: string } | null}
 */
export function parseAgentFile(text) {
  const lines = text.split('\n');
  if (lines[0]?.trim() !== '---') return null;
  let i = 1;
  /** @type {string[]} */
  const fmLines = [];
  while (i < lines.length && lines[i].trim() !== '---') {
    fmLines.push(lines[i]);
    i += 1;
  }
  if (i >= lines.length) return null; // no closing fence
  const body = lines.slice(i + 1).join('\n');
  /** @type {Frontmatter} */
  const frontmatter = { raw: fmLines.join('\n') };
  for (const line of fmLines) {
    const colonIdx = line.indexOf(':');
    if (colonIdx === -1) continue;
    const field = line.slice(0, colonIdx).trim();
    let value = line.slice(colonIdx + 1).trim();
    // Strip surrounding YAML quotes for the scalar fields we keep.
    value = value.replace(/^['"](.*)['"]$/, '$1');
    // Array-valued model fallback (e.g. model: ['A', 'B']): keep the first.
    if (field === 'model' && value.startsWith('[')) {
      const q = value.indexOf("'");
      const dq = value.indexOf('"');
      let start = -1;
      let quote = '';
      if (q !== -1 && (dq === -1 || q < dq)) { start = q; quote = "'"; }
      else if (dq !== -1) { start = dq; quote = '"'; }
      if (start !== -1) {
        const end = value.indexOf(quote, start + 1);
        if (end !== -1) frontmatter.model = value.slice(start + 1, end);
      }
      continue;
    }
    // Scalar assignment — explicit per-field, no dynamic key (object-injection safe).
    if (field === 'name') frontmatter.name = value;
    else if (field === 'description') frontmatter.description = value;
    else if (field === 'model') frontmatter.model = value;
  }
  return { frontmatter, body };
}

/**
 * Render a CLI agent file from a parsed Chat agent.
 * Frontmatter is normalized to { name, description, model }; the body is verbatim.
 * @param {Frontmatter} frontmatter
 * @param {string} body
 * @param {string} sourceRel  Repo-relative source path, for the generated header.
 * @returns {string}
 */
export function renderCliAgent(frontmatter, body, sourceRel) {
  // Frontmatter MUST be first (line 1 = `---`); Copilot CLI agent discovery
  // requires it. The generated-note lives as YAML comments INSIDE the block so
  // `strip_frontmatter` (prompt mode) removes it along with the frontmatter.
  const fm =
    '---\n' +
    '# GENERATED by scripts/generate-cli-agents.mjs — do not edit by hand.\n' +
    `# Source of truth: ${sourceRel}\n` +
    '# Regenerate: `node scripts/generate-cli-agents.mjs`.\n' +
    `name: ${frontmatter.name ?? ''}\n` +
    `description: ${frontmatter.description ?? ''}\n` +
    (frontmatter.model ? `model: ${frontmatter.model}\n` : '') +
    '---\n\n';
  return `${fm}${body.trim()}\n`;
}

/**
 * Generate all CLI agent files from their Chat sources.
 * @param {string[]} _args CLI args (without node/script).
 * @param {{ agents?: Array<{ source: string, target: string }>, rootOverride?: string }} [opts]
 * @returns {Promise<number>} exit code
 */
export async function main(_args, opts = {}) {
  const root = opts.rootOverride ?? ROOT;
  const agents = opts.agents ?? DEFAULT_AGENTS;

  /** @param {string} rel @returns {string} */
  const p = (rel) => resolve(root, rel);

  /** @type {string[]} */
  const written = [];
  for (const { source, target } of agents) {
    const sourcePath = p(source);
    const text = readTextFileSync(sourcePath);
    const parsed = parseAgentFile(text);
    if (!parsed) {
      process.stderr.write(`generate-cli-agents: could not parse frontmatter in ${source}\n`);
      return 1;
    }
    const rendered = renderCliAgent(parsed.frontmatter, parsed.body, source);
    const targetPath = p(target);
    ensureDirSync(dirname(targetPath));
    let existing = '';
    try {
      existing = readTextFileSync(targetPath);
    } catch {
      // First generation; target does not exist yet.
      existing = '';
    }
    if (rendered !== existing) {
      writeTextFileSync(targetPath, rendered);
      written.push(target);
    }
  }

  if (written.length === 0) {
    process.stdout.write('generate-cli-agents: all CLI agents already up to date.\n');
  } else {
    process.stdout.write(`generate-cli-agents: wrote ${written.length} file(s):\n`);
    for (const f of written) process.stdout.write(`  ${f}\n`);
  }
  return 0;
}

if (isMainModule(import.meta.url)) {
  assertNodeVersion();
  main(process.argv.slice(2)).then((code) => process.exit(code));
}
